package com.example.Stomspringtut;

public class HelloMessage {

	  private String name;
	  private String msg;

	  public HelloMessage() {
	  }

	  public HelloMessage(String name) {
	    this.name = name;
	  }
	  


	  public String getName() {
	    return name;
	  }	  
	   

	  public void setName(String name) {
	    this.name = name;
	  }
	  
	  public String getMsg() {
		    return msg;
		  }	 
	  
	  public void setMsg(String msg) {
		    this.msg = msg;
		  }
	  
	}